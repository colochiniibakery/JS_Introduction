//introduction from switch 

const parametros='numero';
   


switch(parametros){
    case 1 : console.log('el valor es 1000'); break;
    case 2 : console.log('el valor es 10000'); break;
    case 3 : console.log('el valor es 100000'); break;
    case 4 : console.log('el valor es 10000000'); break;
    case 5 : console.log('el valor es 100000000'); break;
    case 6 : console.log('el valor es 1000000000'); break;
    case 'numero' : console.log('Haz ingresado un numero incorrecto'); break;

    default:
    console.log('aun no haz pagado');
    break;
}


